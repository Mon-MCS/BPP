def suma(a,b):
    return a+b

def resta(a,b):
    return a-b

def multiplicacion(a,b):
    return a*b

def division(a,b):
    return a/b

def modulo(a,b):
    return a%b

def raiz(a):
    return a**(1/2)

def potencia(a,b=2):
    return a**b
